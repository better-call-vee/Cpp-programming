///PERFECT BINARY TREE
//A perfect binary tree is indeed a type of full binary tree, but with an
//additional constraint. A perfect binary tree is a binary tree in which every
//node has either 0 or 2 children, and all levels are completely filled. In other
//words, a perfect binary tree is a full binary tree where all levels are filled
//and all leaf nodes are at the same depth.
//
//A perfect binary tree has the property that the number of nodes in the tree is
//equal to 2^h - 1, where h is the height of the tree. This means that a perfect
//binary tree with height h has exactly 2^h leaf nodes. Additionally, the height
//of a perfect binary tree can be calculated as log2(n + 1), where n is the number
//of nodes in the tree.
